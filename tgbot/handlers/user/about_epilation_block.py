@router.callback_query(F.data.split(":")[0] == "about_epil")
async def choice_gender(callback: CallbackQuery):
    epil_type = callback.data.split(":")[1]
    if len(callback.data.split(":")) == 2:
        user = await ClientsDAO.get_one_or_none(user_id=str(callback.from_user.id))
        gender = "boys" if user["gender"] == "male" else "girls"
        step = 1
    else:
        gender = callback.data.split(":")[2]
        step = int(callback.data.split(":")[3])
    if gender == "boys":
        if epil_type == "laser":
            if step == 1:
                await laser_boys_1(user_id=callback.from_user.id)
            if step == 2:
                await laser_boys_2(user_id=callback.from_user.id)
            if step == 3:
                await laser_boys_3(user_id=callback.from_user.id)
        if epil_type == "bio":
            if step == 1:
                await bio_boys_1(user_id=callback.from_user.id)
            if step == 2:
                await bio_boys_2(user_id=callback.from_user.id)
    if gender == "girls":
        if epil_type == "laser":
            if step == 1:
                await laser_girls_1(user_id=callback.from_user.id)
            if step == 2:
                await laser_girls_2(user_id=callback.from_user.id)
            if step == 3:
                await laser_girls_3(user_id=callback.from_user.id)
        if epil_type == "bio":
            if step == 1:
                await bio_girls_1(user_id=callback.from_user.id)
            if step == 2:
                await bio_girls_2(user_id=callback.from_user.id)


